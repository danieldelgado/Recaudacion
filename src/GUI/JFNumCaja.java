    private ComboBoxModel listarCajas() {

        ModeloComboCaja mcc = new ModeloComboCaja();
        List<Caja> tipos;

        try {
            tipos = adCaja.listar();
            mcc.setTipos(tipos);
            mcc.setSelectedItem(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            adCaja = null;
        }
        return mcc;
    }

