    private void btnIniciarRecaudacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarRecaudacionActionPerformed
         if (this.cboCajas.getSelectedIndex() != -1) {
           
           Recaudacion objRec;
           ModeloComboCaja mcc;
           mcc = (ModeloComboCaja) this.cboCajas.getModel();
           
           objRec = new Recaudacion();
           objRec.setCodigoPersona(Integer.parseInt(lblcodUsuario.getText()));
           objRec.setCaja(mcc.getTipos().get(this.cboCajas.getSelectedIndex()));
           
           adRecaudacion = new ADRecaudacion();
             try {
                  if (adRecaudacion.registrar(objRec)) {
                    JOptionPane.showMessageDialog(this, "Recaudacion Inicializada");
                    JFPrincipal from = new JFPrincipal();
                    Recaudacion objR;
                    objR = new Recaudacion();
                    objR = adRecaudacion.leer(objRec);
                    from.lblcodRecaudacion.setText(String.valueOf(objR.getCodigo()));
                    from.setVisible(true);
                    this.setVisible(false);

                } else {
                    JOptionPane.showMessageDialog(this, "Error al Iniciar Recaudacion");
                }
             } catch (Exception e) {
                 JOptionPane.showMessageDialog(this, e.getMessage());
             }
             
        }else{
              JOptionPane.showMessageDialog(null, "Escoja Numero de Caja");
        }
    }//GEN-LAST:event_btnIniciarRecaudacionActionPerformed

