    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int opt = JOptionPane.showConfirmDialog(this, "¬øEsta Seguro de Modificar los Datos?", "", JOptionPane.YES_NO_OPTION);

        CuentaBancaria objCB;
        ModeloComboBanco mcb;
        mcb = (ModeloComboBanco) this.cboBancos.getModel();

        java.sql.Date FechaApert = new java.sql.Date(dtpFechaApert.getDate().getTime());

        objCB = new CuentaBancaria();
        objCB.setCodigo(this.actual.getCod());
        objCB.setNumero(txtNumCuenta.getText());
        objCB.setFechaApertura(FechaApert);
        objCB.setDenominacion(txtDenominacion.getText());
        objCB.setMontoInicial(Double.parseDouble(txtMontoIni.getText()));
        objCB.setCodigoInterbancario(txtCodInterBancario.getText());
        objCB.setBanco(mcb.getBanco().get(this.cboBancos.getSelectedIndex()));

        adCuentaBanc = new ADCuentaBancaria();

        if (opt == JOptionPane.YES_OPTION) {
            if (Validacion() == true) {
                try {
                    adCuentaBanc.modificar(objCB);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }

            } else {
                JOptionPane.showMessageDialog(null, FaltaLlenar);
            }
            Actualizar_Tabla();
        }
        btnGuardar.setVisible(false);
        btnModificar.setVisible(true);
        DesabilitarCampos();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void presentarDatos() {
        adCuentaBanc = new ADCuentaBancaria();
        ModeloComboBanco mcb;

        try {
            this.actual = adCuentaBanc.leer(this.actual);
            if (this.actual != null) {
                mcb = (ModeloComboBanco) this.cboBancos.getModel();
                mcb.seleccionarBanco(this.actual.getBanco());
                this.txtNumCuenta.setText(this.actual.getNumero());
                this.dtpFechaApert.setDate(this.actual.getFechaApertura());
                this.txtDenominacion.setText(this.actual.getDenominacion());
                this.txtMontoIni.setText(String.valueOf(this.actual.getMontoInicial()));
                this.txtCodInterBancario.setText(this.actual.getCodigoInterbancario());

            } else {
                JOptionPane.showMessageDialog(null, "No se encontro");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            adCuentaBanc = null;
        }
    }
