    private void Actualizar_Tabla() {
        List<CuentaBancaria> CB;

        try {
            CB = adCuentaBanc.ListarTabla();
            this.mtc.setCuentaB(CB);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } finally {
            adCuentaBanc = null;
        }
    }

    public boolean Validacion() {

        boolean Completo = true;
        if (this.txtNumCuenta.getText().isEmpty()) {
            FaltaLlenar += "ERROR : CAMPO Numero de Cuenta ESTA VACIO\n";
            Completo = false;
        }
        if (this.cboBancos.getSelectedIndex() == -1) {
            FaltaLlenar += "ERROR : CAMPO Banco ESTA VACIO\n";
            Completo = false;
        }
        if (this.txtCodInterBancario.getText().isEmpty()) {
            FaltaLlenar += "ERROR : CAMPO Codigo Interbancario ESTA VACIO\n";
            Completo = false;
        }
        if (this.dtpFechaApert.getDate() == null) {
            FaltaLlenar += "ERROR : CAMPO FECHA APERTURA ESTA VACIO\n";
            Completo = false;
        }
        if (this.txtMontoIni.getText().isEmpty()) {
            FaltaLlenar += "ERROR : CAMPO MONTO INICIAL ESTA VACIO\n";
            Completo = false;
        }
        if (this.txtDenominacion.getText().isEmpty()) {
            FaltaLlenar += "ERROR : CAMPO DENOMINACION ESTA VACIO\n";
            Completo = false;
        }

        return Completo;
    }

